public class FloatOps {
    public static void main(String[] args) {
        double a = 0.1;
        double b = 1 - 9.0 / 10;
        System.out.println(a);
        System.out.println(b);

        double d1 = 1.2 + 24.0 / 5;
        double d2 = 1.2 + 24 / 5.0;

        System.out.println(d1);
        System.out.println(d2);
    }
}
